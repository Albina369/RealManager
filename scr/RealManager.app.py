@app.route('/webhook', methods=['POST'])
def webhook_post():
    data = request.json
    if 'message' in data and 'text' in data['message']:
        chat_id = data['message']['chat']['id']
        text = data['message']['text']
        reply = handle_message(chat_id, text)
        send_message(chat_id, reply)
    return 'ok', 200

